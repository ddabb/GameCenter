function convertLevel(grid) {
  const result = Array.from({length: 5}, () => Array(4).fill(0));
  const claimed = Array.from({length: 5}, () => Array(4).fill(false));
  
  let vIdx = 0, pIdx = 0, hIdx = 0;
  
  for (let r = 0; r < 5; r++) {
    for (let c = 0; c < 4; c++) {
      if (claimed[r][c] || grid[r][c] === 0) continue;
      
      const val = grid[r][c];
      let id, h, w;
      
      if (val === 1) {
        // 曹操 2x2
        id = 1; h = 2; w = 2;
      } else if (val === 2) {
        // 横将 1x2
        hIdx++;
        id = hIdx === 1 ? 2 : 50 + hIdx; // 第一个横将是关羽(ID=2)，后续用50+
        h = 1; w = 2;
      } else if (val === 3) {
        // 竖将 2x1
        id = 3 + vIdx; vIdx++;
        h = 2; w = 1;
      } else if (val === 4) {
        // 卒 1x1
        id = 7 + pIdx; pIdx++;
        h = 1; w = 1;
      } else {
        continue;
      }
      
      // 填充 result 和 claimed
      for (let dr = 0; dr < h; dr++) {
        for (let dc = 0; dc < w; dc++) {
          if (r + dr < 5 && c + dc < 4) {
            result[r + dr][c + dc] = id;
            claimed[r + dr][c + dc] = true;
          }
        }
      }
    }
  }
  
  return result;
}

