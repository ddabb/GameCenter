/**
 * 获取棋子名称
 */
export function getPieceName(id: number): string {
    return PieceNames[id] || `棋${id}`;
}

/**
 * 获取第 n 关的棋盘（1-indexed）
 * 返回 5×4 的二维数组
 */
export function getLevel(n: number): number[][] {
    if (n < 1 || n > Levels.length) n = 1;
    return Levels[n - 1].map(row => [...row]);  // 复制
}

