    /// <summary>
    /// 获取第 n 关的棋盘（1-indexed，1-30）
    /// </summary>
    public static int[,] GetLevel(int n)
    {
        if (n < 1 || n > Levels.Length) n = 1;
        var src = Levels[n - 1];
        var grid = new int[5, 4];
        for (int r = 0; r < 5; r++)
            for (int c = 0; c < 4; c++)
                grid[r, c] = src[r][c];
        return grid;
    }
    
